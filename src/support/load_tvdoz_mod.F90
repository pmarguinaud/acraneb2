MODULE LOAD_TVDOZ_MOD

USE YOMVDOZ, ONLY : TVDOZ
USE PARKIND1, ONLY : JPRB, JPRD

INTERFACE LOAD
MODULE PROCEDURE LOAD_TVDOZ
END INTERFACE

CONTAINS

SUBROUTINE LOAD_TVDOZ (KLUN, YD)

IMPLICIT NONE
INTEGER, INTENT (IN) :: KLUN
TYPE (TVDOZ), INTENT (OUT) :: YD
REAL(KIND=JPRD) :: ZTEMP0
REAL(KIND=JPRD), ALLOCATABLE :: ZTEMP1 (:)
ALLOCATE (ZTEMP1 (LBOUND (YD%VDHJS, 1):UBOUND (YD%VDHJS, 1)))
READ (KLUN) ZTEMP1
YD%VDHJS = ZTEMP1
DEALLOCATE (ZTEMP1)
ALLOCATE (ZTEMP1 (LBOUND (YD%VDHJH, 1):UBOUND (YD%VDHJH, 1)))
READ (KLUN) ZTEMP1
YD%VDHJH = ZTEMP1
DEALLOCATE (ZTEMP1)
ALLOCATE (ZTEMP1 (LBOUND (YD%VDHNS, 1):UBOUND (YD%VDHNS, 1)))
READ (KLUN) ZTEMP1
YD%VDHNS = ZTEMP1
DEALLOCATE (ZTEMP1)
ALLOCATE (ZTEMP1 (LBOUND (YD%VDHNH, 1):UBOUND (YD%VDHNH, 1)))
READ (KLUN) ZTEMP1
YD%VDHNH = ZTEMP1
DEALLOCATE (ZTEMP1)
ALLOCATE (ZTEMP1 (LBOUND (YD%VDPJS, 1):UBOUND (YD%VDPJS, 1)))
READ (KLUN) ZTEMP1
YD%VDPJS = ZTEMP1
DEALLOCATE (ZTEMP1)
ALLOCATE (ZTEMP1 (LBOUND (YD%VDPJH, 1):UBOUND (YD%VDPJH, 1)))
READ (KLUN) ZTEMP1
YD%VDPJH = ZTEMP1
DEALLOCATE (ZTEMP1)
ALLOCATE (ZTEMP1 (LBOUND (YD%VDPNS, 1):UBOUND (YD%VDPNS, 1)))
READ (KLUN) ZTEMP1
YD%VDPNS = ZTEMP1
DEALLOCATE (ZTEMP1)
ALLOCATE (ZTEMP1 (LBOUND (YD%VDPNH, 1):UBOUND (YD%VDPNH, 1)))
READ (KLUN) ZTEMP1
YD%VDPNH = ZTEMP1
DEALLOCATE (ZTEMP1)
ALLOCATE (ZTEMP1 (LBOUND (YD%VDEJS, 1):UBOUND (YD%VDEJS, 1)))
READ (KLUN) ZTEMP1
YD%VDEJS = ZTEMP1
DEALLOCATE (ZTEMP1)
ALLOCATE (ZTEMP1 (LBOUND (YD%VDEJH, 1):UBOUND (YD%VDEJH, 1)))
READ (KLUN) ZTEMP1
YD%VDEJH = ZTEMP1
DEALLOCATE (ZTEMP1)
ALLOCATE (ZTEMP1 (LBOUND (YD%VDENS, 1):UBOUND (YD%VDENS, 1)))
READ (KLUN) ZTEMP1
YD%VDENS = ZTEMP1
DEALLOCATE (ZTEMP1)
ALLOCATE (ZTEMP1 (LBOUND (YD%VDENH, 1):UBOUND (YD%VDENH, 1)))
READ (KLUN) ZTEMP1
YD%VDENH = ZTEMP1
DEALLOCATE (ZTEMP1)
ALLOCATE (ZTEMP1 (LBOUND (YD%VDAJS, 1):UBOUND (YD%VDAJS, 1)))
READ (KLUN) ZTEMP1
YD%VDAJS = ZTEMP1
DEALLOCATE (ZTEMP1)
ALLOCATE (ZTEMP1 (LBOUND (YD%VDAJH, 1):UBOUND (YD%VDAJH, 1)))
READ (KLUN) ZTEMP1
YD%VDAJH = ZTEMP1
DEALLOCATE (ZTEMP1)
ALLOCATE (ZTEMP1 (LBOUND (YD%VDANS, 1):UBOUND (YD%VDANS, 1)))
READ (KLUN) ZTEMP1
YD%VDANS = ZTEMP1
DEALLOCATE (ZTEMP1)
ALLOCATE (ZTEMP1 (LBOUND (YD%VDANH, 1):UBOUND (YD%VDANH, 1)))
READ (KLUN) ZTEMP1
YD%VDANH = ZTEMP1
DEALLOCATE (ZTEMP1)
ALLOCATE (ZTEMP1 (LBOUND (YD%VDNJS, 1):UBOUND (YD%VDNJS, 1)))
READ (KLUN) ZTEMP1
YD%VDNJS = ZTEMP1
