ALLOCATE (ZTEMP1 (LBOUND (YD%VDNNS, 1):UBOUND (YD%VDNNS, 1)))
READ (KLUN) ZTEMP1
YD%VDNNS = ZTEMP1
DEALLOCATE (ZTEMP1)
ALLOCATE (ZTEMP1 (LBOUND (YD%VDNNH, 1):UBOUND (YD%VDNNH, 1)))
READ (KLUN) ZTEMP1
YD%VDNNH = ZTEMP1
DEALLOCATE (ZTEMP1)
READ (KLUN) ZTEMP0
YD%VOZNJ = ZTEMP0
READ (KLUN) ZTEMP0
YD%VOZHS = ZTEMP0
READ (KLUN) YD%LRDIFOZ
READ (KLUN) YD%LRDEPOZ
END SUBROUTINE


END MODULE
