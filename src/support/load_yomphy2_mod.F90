MODULE LOAD_YOMPHY2_MOD

USE YOMPHY2

CONTAINS

SUBROUTINE LOAD_YOMPHY2 (KLUN)
USE LOAD_TPHY2_MOD

IMPLICIT NONE
INTEGER, INTENT (IN) :: KLUN
LOGICAL :: LYRPHY2
READ (KLUN) LYRPHY2
IF (LYRPHY2) THEN
  ALLOCATE (YRPHY2)
  CALL LOAD (KLUN, YRPHY2)
ELSE
  NULLIFY (YRPHY2)
ENDIF
END SUBROUTINE


END MODULE
