MODULE LOAD_TTOPH_MOD

USE YOMTOPH, ONLY : TTOPH
USE PARKIND1, ONLY : JPRB, JPRD

INTERFACE LOAD
MODULE PROCEDURE LOAD_TTOPH
END INTERFACE

CONTAINS

SUBROUTINE LOAD_TTOPH (KLUN, YD)

IMPLICIT NONE
INTEGER, INTENT (IN) :: KLUN
TYPE (TTOPH), INTENT (OUT) :: YD
REAL(KIND=JPRD) :: ZTEMP0
REAL(KIND=JPRD), ALLOCATABLE :: ZTEMP1 (:)
INTEGER :: IL1(1), IU1(1)
LOGICAL :: LRMESOQ, LRMESOT, LRMESOU
READ (KLUN) LRMESOU
IF (LRMESOU) THEN
  READ (KLUN) IL1
  READ (KLUN) IU1
  ALLOCATE (YD%RMESOU (IL1(1):IU1(1)))
  ALLOCATE (ZTEMP1 (LBOUND (YD%RMESOU, 1):UBOUND (YD%RMESOU, 1)))
  READ (KLUN) ZTEMP1
  YD%RMESOU = ZTEMP1
  DEALLOCATE (ZTEMP1)
ENDIF
READ (KLUN) LRMESOT
IF (LRMESOT) THEN
  READ (KLUN) IL1
  READ (KLUN) IU1
  ALLOCATE (YD%RMESOT (IL1(1):IU1(1)))
  ALLOCATE (ZTEMP1 (LBOUND (YD%RMESOT, 1):UBOUND (YD%RMESOT, 1)))
  READ (KLUN) ZTEMP1
  YD%RMESOT = ZTEMP1
  DEALLOCATE (ZTEMP1)
ENDIF
READ (KLUN) LRMESOQ
IF (LRMESOQ) THEN
  READ (KLUN) IL1
  READ (KLUN) IU1
  ALLOCATE (YD%RMESOQ (IL1(1):IU1(1)))
  ALLOCATE (ZTEMP1 (LBOUND (YD%RMESOQ, 1):UBOUND (YD%RMESOQ, 1)))
  READ (KLUN) ZTEMP1
  YD%RMESOQ = ZTEMP1
  DEALLOCATE (ZTEMP1)
ENDIF
READ (KLUN) ZTEMP0
YD%RFMESOQ = ZTEMP0
READ (KLUN) YD%NTQSAT
READ (KLUN) YD%NTDIFU
READ (KLUN) YD%NTCOEF
READ (KLUN) YD%NTDRAG
READ (KLUN) YD%NTCVIM
READ (KLUN) YD%NTPLUI
READ (KLUN) YD%NTRADI
READ (KLUN) YD%NTNEBU
READ (KLUN) YD%NTOZON
READ (KLUN) YD%NTDRME
READ (KLUN) YD%NTCOET
READ (KLUN) YD%NTAJUC
READ (KLUN) YD%NTRELAXT
READ (KLUN) YD%NTRELAXQ
READ (KLUN) YD%NTRELAXU
READ (KLUN) ZTEMP0
YD%ETQSAT = ZTEMP0
READ (KLUN) ZTEMP0
YD%ETDIFU = ZTEMP0
READ (KLUN) ZTEMP0
YD%ETCOEF = ZTEMP0
READ (KLUN) ZTEMP0
YD%ETDRAG = ZTEMP0
READ (KLUN) ZTEMP0
YD%ETCVIM = ZTEMP0
READ (KLUN) ZTEMP0
YD%ETPLUI = ZTEMP0
READ (KLUN) ZTEMP0
YD%ETRADI = ZTEMP0
READ (KLUN) ZTEMP0
YD%ETNEBU = ZTEMP0
READ (KLUN) ZTEMP0
YD%ETOZON = ZTEMP0
READ (KLUN) ZTEMP0
YD%ETDRME = ZTEMP0
READ (KLUN) ZTEMP0
YD%ETCOET = ZTEMP0
READ (KLUN) ZTEMP0
YD%ETAJUC = ZTEMP0
READ (KLUN) ZTEMP0
YD%ETRELAXT = ZTEMP0
READ (KLUN) ZTEMP0
YD%ETRELAXQ = ZTEMP0
READ (KLUN) ZTEMP0
YD%ETRELAXU = ZTEMP0
READ (KLUN) ZTEMP0
YD%XDRMUK = ZTEMP0
READ (KLUN) ZTEMP0
YD%XDRMUX = ZTEMP0
READ (KLUN) ZTEMP0
YD%XDRMUP = ZTEMP0
READ (KLUN) ZTEMP0
YD%XDRMTK = ZTEMP0
READ (KLUN) ZTEMP0
YD%XDRMTX = ZTEMP0
READ (KLUN) ZTEMP0
YD%XDRMTP = ZTEMP0
READ (KLUN) ZTEMP0
YD%XDRMQK = ZTEMP0
READ (KLUN) ZTEMP0
YD%XDRMQP = ZTEMP0
READ (KLUN) ZTEMP0
YD%XDRMQX = ZTEMP0
READ (KLUN) ZTEMP0
YD%RCLX = ZTEMP0
READ (KLUN) ZTEMP0
YD%TPSCLIM = ZTEMP0
END SUBROUTINE


END MODULE
