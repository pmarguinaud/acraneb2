MODULE LOAD_TLOUIS_MOD

USE YOMLOUIS, ONLY : TLOUIS
USE PARKIND1, ONLY : JPRB, JPRD

INTERFACE LOAD
MODULE PROCEDURE LOAD_TLOUIS
END INTERFACE

CONTAINS

SUBROUTINE LOAD_TLOUIS (KLUN, YD)

IMPLICIT NONE
INTEGER, INTENT (IN) :: KLUN
TYPE (TLOUIS), INTENT (OUT) :: YD
REAL(KIND=JPRD) :: ZTEMP0
READ (KLUN) ZTEMP0
YD%RLOUIS_S0 = ZTEMP0
READ (KLUN) ZTEMP0
YD%PLOUIS_S0 = ZTEMP0
READ (KLUN) ZTEMP0
YD%RLOUIS_GU1 = ZTEMP0
READ (KLUN) ZTEMP0
YD%RLOUIS_GU2 = ZTEMP0
READ (KLUN) ZTEMP0
YD%PLOUIS_GU1 = ZTEMP0
READ (KLUN) ZTEMP0
YD%PLOUIS_GU2 = ZTEMP0
READ (KLUN) ZTEMP0
YD%RLOUIS_GS1 = ZTEMP0
READ (KLUN) ZTEMP0
YD%RLOUIS_GS2 = ZTEMP0
READ (KLUN) ZTEMP0
YD%RLOUIS_GS3 = ZTEMP0
READ (KLUN) ZTEMP0
YD%RLOUIS_GS4 = ZTEMP0
READ (KLUN) ZTEMP0
YD%PLOUIS_GS1 = ZTEMP0
READ (KLUN) ZTEMP0
YD%PLOUIS_GS2 = ZTEMP0
READ (KLUN) ZTEMP0
YD%PLOUIS_GS3 = ZTEMP0
READ (KLUN) ZTEMP0
YD%PLOUIS_GS4 = ZTEMP0
END SUBROUTINE


END MODULE
