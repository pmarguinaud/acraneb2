MODULE LOAD_TCVMNH_MOD

USE YOMCVMNH, ONLY : TCVMNH
USE PARKIND1, ONLY : JPRB, JPRD

INTERFACE LOAD
MODULE PROCEDURE LOAD_TCVMNH
END INTERFACE

CONTAINS

SUBROUTINE LOAD_TCVMNH (KLUN, YD)

IMPLICIT NONE
INTEGER, INTENT (IN) :: KLUN
TYPE (TCVMNH), INTENT (OUT) :: YD
REAL(KIND=JPRD) :: ZTEMP0
READ (KLUN) YD%LDEEP
READ (KLUN) YD%LSHALLOW
READ (KLUN) YD%LDIAGCONV
READ (KLUN) YD%LSETTADJ
READ (KLUN) YD%LREFRESH_ALL
READ (KLUN) YD%LDOWN
READ (KLUN) YD%LSMOOTH
READ (KLUN) ZTEMP0
YD%OTADJD = ZTEMP0
READ (KLUN) ZTEMP0
YD%OTADJS = ZTEMP0
READ (KLUN) YD%NSETENS
READ (KLUN) YD%NIICE
READ (KLUN) ZTEMP0
YD%XA25 = ZTEMP0
READ (KLUN) ZTEMP0
YD%XCRAD = ZTEMP0
READ (KLUN) ZTEMP0
YD%XCDEPTH = ZTEMP0
READ (KLUN) ZTEMP0
YD%XCDEPTH_D = ZTEMP0
READ (KLUN) ZTEMP0
YD%XDTPERT = ZTEMP0
READ (KLUN) ZTEMP0
YD%XENTR = ZTEMP0
READ (KLUN) ZTEMP0
YD%XZLCL = ZTEMP0
READ (KLUN) ZTEMP0
YD%XZPBL = ZTEMP0
READ (KLUN) ZTEMP0
YD%XWTRIG = ZTEMP0
READ (KLUN) ZTEMP0
YD%XNHGAM = ZTEMP0
READ (KLUN) ZTEMP0
YD%XTFRZ1 = ZTEMP0
READ (KLUN) ZTEMP0
YD%XTFRZ2 = ZTEMP0
READ (KLUN) ZTEMP0
YD%XSTABT = ZTEMP0
READ (KLUN) ZTEMP0
YD%XSTABC = ZTEMP0
READ (KLUN) ZTEMP0
YD%XAW = ZTEMP0
READ (KLUN) ZTEMP0
YD%XBW = ZTEMP0
READ (KLUN) ZTEMP0
YD%XATPERT = ZTEMP0
READ (KLUN) ZTEMP0
YD%XBTPERT = ZTEMP0
END SUBROUTINE


END MODULE
