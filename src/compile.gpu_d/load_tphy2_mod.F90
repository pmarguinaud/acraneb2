MODULE LOAD_TPHY2_MOD

USE YOMPHY2, ONLY : TPHY2
USE PARKIND1, ONLY : JPRB, JPRD

INTERFACE LOAD
MODULE PROCEDURE LOAD_TPHY2
END INTERFACE

CONTAINS

SUBROUTINE LOAD_TPHY2 (KLUN, YD)

IMPLICIT NONE
INTEGER, INTENT (IN) :: KLUN
TYPE (TPHY2), INTENT (OUT) :: YD
REAL(KIND=JPRD) :: ZTEMP0
READ (KLUN) YD%NTSHM
READ (KLUN) YD%NTSML
READ (KLUN) ZTEMP0
YD%TSPHY = ZTEMP0
READ (KLUN) ZTEMP0
YD%XMUCVPP = ZTEMP0
READ (KLUN) ZTEMP0
YD%XMULAF = ZTEMP0
READ (KLUN) ZTEMP0
YD%XDAMP = ZTEMP0
READ (KLUN) ZTEMP0
YD%HCLP = ZTEMP0
READ (KLUN) ZTEMP0
YD%HTCLS = ZTEMP0
READ (KLUN) ZTEMP0
YD%HVCLS = ZTEMP0
READ (KLUN) ZTEMP0
YD%HTSHM = ZTEMP0
READ (KLUN) ZTEMP0
YD%HTSML = ZTEMP0
READ (KLUN) ZTEMP0
YD%HWMOHIGH = ZTEMP0
READ (KLUN) ZTEMP0
YD%HWMOLOW = ZTEMP0
READ (KLUN) ZTEMP0
YD%GZ0RAF = ZTEMP0
READ (KLUN) ZTEMP0
YD%FACRAF = ZTEMP0
READ (KLUN) ZTEMP0
YD%HTKERAF = ZTEMP0
READ (KLUN) YD%LRAFTUR
READ (KLUN) YD%LMULAF
READ (KLUN) YD%LWMOCLOUD
READ (KLUN) YD%LRAFTKE
END SUBROUTINE


END MODULE
