MODULE LOAD_YOMPHY1_MOD

USE YOMPHY1

CONTAINS

SUBROUTINE LOAD_YOMPHY1 (KLUN)
USE LOAD_TPHY1_MOD

IMPLICIT NONE
INTEGER, INTENT (IN) :: KLUN
LOGICAL :: LYRPHY1
READ (KLUN) LYRPHY1
IF (LYRPHY1) THEN
  ALLOCATE (YRPHY1)
  CALL LOAD (KLUN, YRPHY1)
ELSE
  NULLIFY (YRPHY1)
ENDIF
END SUBROUTINE


END MODULE
